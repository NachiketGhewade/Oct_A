package arrays;

public class Arrays {
	public static void main(String[] args) {
		
		//declaration 
		String n[] ;
		
		n = new String [5];
		
		n[0]="nachiket";
		n[1]="snehal";
		n[2]="kunal";
		n[3]="apurva";
		n[4]="prajakta";
		
		System.out.println(n[2]);
		
		for (String a :n) {
			System.out.println(a);
		}
		
		
	}


}
